def tripData(laneDepartureList, laneSwitchList, accelDecelList, trafficViolationList, turnSigFailList, speedList, speedLimitList):
    violationPoints = 0
    #lane departure
    laneDepartureViolations = sum(laneDepartureList)
    laneDeparturePoints = laneDepartureViolations * 3
    violationPoints += laneDeparturePoints

    #lane switch
    laneSwitchViolations = sum(laneSwitchList)
    percent = sum(laneSwitchList) / len(laneSwitchList) * 100
    laneSwitchPoints = (percent // 4) * 2
    violationPoints += laneSwitchPoints
    
    #acceleration/deceleration
    accelDecels = 0
    for i in accelDecelList:
        if i >= 20:
            accelDecels += 1
    accelDecelPoints = accelDecels * 2
    violationPoints += accelDecelPoints

    #traffic violations
    trafficViolations = sum(trafficViolationList)
    trafficPoints = trafficViolations * 5
    violationPoints += trafficPoints
    
    #turn signal
    turnSignalViolations = sum(turnSigFailList)
    turnSignalPoints = turnSignalViolations
    violationPoints += turnSignalPoints

    #speed violation
    speedWithin10, speedWithin20, speedGreater20 = 0, 0, 0
    for i in range(len(speedList)):
        currSpeed = abs(speedList[i] - speedLimitList[i])
        if currSpeed > 5 and currSpeed <= 10:
            speedWithin10 += 1
        if currSpeed > 10 and currSpeed <= 20:
            speedWithin20 += 1
        if currSpeed > 20:
            speedGreater20 += 1
    speedWithin10Points, speedWithin20Points, speedGreater20Points = 0, 0, 0
    
    percent = speedWithin10/len(speedList) * 100
    speedWithin10Points = (percent // 10)
    percent = speedWithin20/len(speedList) * 100
    speedWithin20Points = (percent // 3)
    speedGreater20Points += speedGreater20
    violationPoints += (speedWithin10Points + speedWithin20Points + speedGreater20Points)

    laneDeparturePercentages = laneDeparturePoints / violationPoints if violationPoints != 0 else 0
    laneSwitchPercentages = laneSwitchPoints / violationPoints if violationPoints != 0 else 0
    accelDecelPercentages = accelDecelPoints / violationPoints if violationPoints != 0 else 0
    trafficPercentages = trafficPoints / violationPoints if violationPoints != 0 else 0
    turnSignalPercentages = turnSignalPoints / violationPoints if violationPoints != 0 else 0
    speedWithin10Percentages = speedWithin10Points / violationPoints if violationPoints != 0 else 0
    speedWithin20Percentages = speedWithin20Points / violationPoints if violationPoints != 0 else 0
    speedGreater20Percentages = speedGreater20Points / violationPoints if violationPoints != 0 else 0
    #Return total points, number of times you drifted lanes, number of times you switched lanes,
    #number of times your acceleration/deceleration exceeded 20mph/s, number of times you ran a red/stop,
    #number of times you didn't use indicators to turn lanes, number of seconds drove within 5 to 10 above limit,
    #number of seconds drove within 10 to 20 above limit, number of seconds drove above 20 second limit
    totalPoints = 100 - (9 * violationPoints)
    return [totalPoints, laneDepartureViolations, laneSwitchViolations, accelDecels, trafficViolations,
     turnSignalViolations, speedWithin10, speedWithin20, speedGreater20, laneDeparturePercentages,
     laneSwitchPercentages, accelDecelPercentages, trafficPercentages, turnSignalPercentages,
     speedWithin10Percentages, speedWithin20Percentages, speedGreater20Percentages]

